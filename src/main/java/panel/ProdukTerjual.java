    private void loadProdukTerjual() {

    try {
        Date tglMulai = (Date) spnMulai.getValue();
        Date tglAkhir = (Date) spnAkhir.getValue();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String mulai = sdf.format(tglMulai);
        String akhir = sdf.format(tglAkhir);

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        String sql =
            "SELECT pj.tanggal, p.id_produk, p.nama_produk, p.id_kategori, " +
            "dp.qty, dp.harga, (dp.qty * dp.harga) AS subtotal, pg.nama AS kasir " +
            "FROM penjualan pj " +
            "JOIN detail_penjualan dp ON pj.id_penjualan = dp.id_penjualan " +
            "JOIN produk p ON dp.id_produk = p.id_produk " +
            "JOIN pengguna pg ON pj.id_pengguna = pg.id_pengguna " +
            "WHERE pj.tanggal BETWEEN ? AND ? " +
            "ORDER BY pj.tanggal ASC";

        Connection con = koneksi.Go();
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, mulai);
        ps.setString(2, akhir);

        ResultSet rs = ps.executeQuery();

        int no = 1;
        while (rs.next()) {
            Object[] row = {
                no++,
                rs.getDate("tanggal"),
                rs.getInt("id_produk"),
                rs.getString("nama_produk"),
                rs.getString("id_kategori"),
                rs.getInt("qty"),
                rs.getDouble("harga"),
                rs.getDouble("subtotal"),
                rs.getString("kasir")
            };
            model.addRow(row);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this,
            "Gagal memuat data produk terjual\n" + e.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE
        );
    }
}
    
    private void initSpinnerTanggal() {

    // Spinner Tanggal Mulai
    SpinnerDateModel modelMulai = new SpinnerDateModel(
        new Date(),          // nilai awal
        null,                // min
        null,                // max
        Calendar.DAY_OF_MONTH
    );
    spnMulai.setModel(modelMulai);
    spnMulai.setEditor(
        new javax.swing.JSpinner.DateEditor(spnMulai, "dd-MM-yyyy")
    );

    // Spinner Tanggal Akhir
    SpinnerDateModel modelAkhir = new SpinnerDateModel(
        new Date(),
        null,
        null,
        Calendar.DAY_OF_MONTH
    );
    spnAkhir.setModel(modelAkhir);
    spnAkhir.setEditor(
        new javax.swing.JSpinner.DateEditor(spnAkhir, "dd-MM-yyyy")
    );
}

