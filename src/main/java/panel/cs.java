    private void loadProduk(String where) {
        modelProduk = (DefaultTableModel) jTableProduk.getModel();
        modelProduk.setRowCount(0);

        try {
            Connection c = koneksi.Go();
            String sql =
            "SELECT p.id_produk, p.nama_produk, p.harga, p.stok, " +
            "k.nama_kategori, p.diskon " +
            "FROM produk p JOIN kategori k ON p.id_kategori = k.id_kategori "
            + where;

            PreparedStatement ps = c.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                modelProduk.addRow(new Object[]{
                    rs.getInt("id_produk"),
                    rs.getString("nama_produk"),
                    rs.getDouble("harga"), 
                    rs.getInt("stok"),
                    rs.getString("nama_kategori"),
                    rs.getDouble("diskon")   
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }


    private void hitungSubtotal() {
        int row = jTableProduk.getSelectedRow();
        if (row == -1) return;

        double harga = Double.parseDouble(
            jTableProduk.getValueAt(row, 2).toString()
        );

        int qty = txtQty.getText().isEmpty()
                ? 0
                : Integer.parseInt(txtQty.getText());

        double subtotal = harga * qty;
        txtSubtotal.setText(String.valueOf(subtotal));
    }

    private void hitungTotal() {
        double total = 0;

        for (int i = 0; i < jTableKeranjang.getRowCount(); i++) {
            total += Double.parseDouble(
                jTableKeranjang.getValueAt(i, 6).toString() // subtotal
            );
        }

        txtTotal.setText(String.valueOf(total));
    }

    
    private void simpanTransaksi(double total, double bayar, double kembali) {
    try {
        Connection c = koneksi.Go();
        c.setAutoCommit(false);

        // INSERT TRANSAKSI
        String sql = "INSERT INTO penjualan ( tanggal, id_pengguna, total) "
                   + "VALUES ( ?, ?, ?)";
        PreparedStatement ps = c.prepareStatement(sql, PreparedStatement.RETURN_GENERATED_KEYS);
        ps.setString(1, jLabelTanggal.getText());
        ps.setInt(2, P.getId());
        ps.setDouble(3, total);
        ps.executeUpdate();
        
        int idPenjualan = 0;
        ResultSet rs = ps.getGeneratedKeys();
        if (rs.next()) {
            idPenjualan = rs.getInt(1);
        }


        // INSERT DETAIL
        for (int i = 0; i < jTableKeranjang.getRowCount(); i++) {
            
            String idProduk = jTableKeranjang.getValueAt(i, 0).toString();
            int qty = Integer.parseInt(jTableKeranjang.getValueAt(i, 2).toString());       // Qty
            double harga = Double.parseDouble(jTableKeranjang.getValueAt(i, 5).toString()); // Harga setelah diskon
            double subtotal = Double.parseDouble(jTableKeranjang.getValueAt(i, 6).toString()); // Subtotal

            
             // Detail
            String sqlDetail =
                "INSERT INTO detail_penjualan (id_penjualan, id_produk, qty, harga, subtotal) "
              + "VALUES (?, ?, ?, ?, ?)";

            PreparedStatement psd = c.prepareStatement(sqlDetail);
            psd.setInt(1, idPenjualan);
            psd.setInt(2, Integer.parseInt(idProduk));
            psd.setInt(3, qty);
            psd.setDouble(4, harga);
            psd.setDouble(5, subtotal);
            psd.executeUpdate();
            
            // UPDATE STOK
            String sqlStok =
                "UPDATE produk SET stok = stok - ? WHERE id_produk = ?";

            PreparedStatement psStok = c.prepareStatement(sqlStok);
            psStok.setInt(1, qty);
            psStok.setString(2, idProduk);
            psStok.executeUpdate();

        }

        c.commit();


    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan transaksi: " + e.getMessage());
    }
}

    
