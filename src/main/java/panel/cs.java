    private void btnTambahKeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahKeranjangActionPerformed
        // TODO add your handling code here:
        int row = jTableProduk.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih produk dulu!");
            return;
        }
        
        if (txtQty.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Qty tidak boleh kosong!");
        return;
        }
        
        int qty = Integer.parseInt(txtQty.getText());
        if (qty <= 0) {
        JOptionPane.showMessageDialog(this, "Qty harus lebih dari 0");
        return;
        }

        int id = Integer.parseInt(jTableProduk.getValueAt(row, 0).toString());
        String nama = jTableProduk.getValueAt(row, 1).toString();
        double harga = Double.parseDouble(jTableProduk.getValueAt(row, 2).toString());
        double diskon = Double.parseDouble(jTableProduk.getValueAt(row, 5).toString()); // 10 = 10%



        double hargaDiskon = harga - (harga * diskon / 100);
        double subtotal = hargaDiskon * qty;

        modelKeranjang = (DefaultTableModel) jTableKeranjang.getModel();
        modelKeranjang.addRow(new Object[]{
            id, nama, qty, harga,diskon,hargaDiskon, subtotal
        });

        hitungTotal();
        txtQty.setText("");
        txtSubtotal.setText("");
    }//GEN-LAST:event_btnTambahKeranjangActionPerformed

