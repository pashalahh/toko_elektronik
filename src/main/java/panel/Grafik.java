    private void LoadData() {
        listTanggal.clear();
        listTotal.clear();

        try {
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String mulai = sdf.format(spnMulai.getValue());
            String sampai = sdf.format(spnAkhir.getValue());
            
            System.out.println("Mulai: " + mulai);
            System.out.println("Sampai: " + sampai);
            
            Connection c = koneksi.Go();
            String sql = """
                SELECT tanggal, SUM(total) AS total_harian
                FROM penjualan
                WHERE tanggal >= ? AND tanggal <= ?
                GROUP BY tanggal
                ORDER BY tanggal
            """;

            PreparedStatement ps = c.prepareStatement(sql);
            ps.setString(1, mulai);
            ps.setString(2, sampai);

            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                listTanggal.add(rs.getString("tanggal"));
                listTotal.add(rs.getDouble("total_harian"));
            }

            System.out.println("Jumlah data: " + listTotal.size());
            System.out.println("Jumlah data: " + listTotal.size());
            System.out.println("Tanggal: " + listTanggal);
            System.out.println("Total: " + listTotal);

            panelGrafik.repaint(); // ðŸ”¥ INI KUNCINYA

        } catch (Exception e) {
            e.printStackTrace();
        }
     
    }
