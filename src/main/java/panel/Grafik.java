    private void LoadData() {
    listTanggal.clear();
    listTotal.clear();

    try {
        Connection c = koneksi.Go();
        String sql = """
            SELECT tanggal, SUM(total) AS total_harian
            FROM penjualan
            WHERE tanggal BETWEEN ? AND ?
            GROUP BY tanggal
            ORDER BY tanggal
        """;

        PreparedStatement ps = c.prepareStatement(sql);
        java.util.Date d1 = (java.util.Date) spnMulai.getValue();
        java.util.Date d2 = (java.util.Date) spnAkhir.getValue();

        ps.setDate(1, new java.sql.Date(d1.getTime()));
        ps.setDate(2, new java.sql.Date(d2.getTime()));


        ResultSet rs = ps.executeQuery();
        while (rs.next()) {
            listTanggal.add(rs.getString("tanggal"));
            listTotal.add(rs.getDouble("total_harian"));
        }

        System.out.println("Jumlah data: " + listTotal.size());

        panelGrafik.repaint(); // ðŸ”¥ WAJIB

    } catch (Exception e) {
        e.printStackTrace();
    }
}

   private void drawGrafik(Graphics g) {
    if (listTotal.isEmpty()) return;

    Graphics2D g2 = (Graphics2D) g;
    int w = panelGrafik.getWidth();
    int h = panelGrafik.getHeight();

    int padding = 40;

    // Cari nilai maksimum
    double max = Collections.max(listTotal);

    // Gambar sumbu
    g2.drawLine(padding, h - padding, w - padding, h - padding); // X
    g2.drawLine(padding, padding, padding, h - padding);        // Y

    int n = listTotal.size();
    int stepX = (w - 2 * padding) / (n - 1);

    int prevX = padding;
    int prevY = h - padding;

    for (int i = 0; i < n; i++) {
        int x = padding + (i * stepX);
        int y = h - padding - (int) ((listTotal.get(i) / max) * (h - 2 * padding));

        g2.fillOval(x - 3, y - 3, 6, 6);

        if (i > 0) {
            g2.drawLine(prevX, prevY, x, y);
        }

        prevX = x;
        prevY = y;
    }
    System.out.println("paintComponent DIPANGGIL");

}


